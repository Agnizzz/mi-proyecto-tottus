    static const int MAX_ADMINS = 20; // Máximo de administradores en el sistema
    Admin administradores[MAX_ADMINS];
    int cantidadAdmins;
    Admin adminActual;
    bool sesionIniciada;

    // Método para cargar los administradores desde el archivo
    void cargarAdministradores() {
        ifstream archivo("Administradores.txt");
        if (!archivo) {
            cout << "Error al abrir el archivo de administradores" << endl;
            return;
        }

        cantidadAdmins = 0;
        string linea;
        int contador = 0;

        while (getline(archivo, linea) && cantidadAdmins < MAX_ADMINS) {
            if (linea.empty()) continue;

            contador++;
            switch (contador % 3) {
            case 1: // Nombre completo
                administradores[cantidadAdmins].nombre = linea;
                break;
            case 2: // Usuario
                administradores[cantidadAdmins].usuario = linea;
                break;
            case 0: // Contraseña (y se completa el admin)
                administradores[cantidadAdmins].contrasena = linea;
                cantidadAdmins++;
                break;
            }
        }

        archivo.close();
    }


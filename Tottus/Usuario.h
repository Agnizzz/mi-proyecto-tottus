// Estructura para almacenar los datos de cada usuario
struct User {
	string correo;
	string nombre;
	string apellido_paterno;
	string apellido_materno;
	string usuario;
	string DNI;
	string celular;
	string contrasena;
};

// Clase para gestionar la autenticación de usuarios
